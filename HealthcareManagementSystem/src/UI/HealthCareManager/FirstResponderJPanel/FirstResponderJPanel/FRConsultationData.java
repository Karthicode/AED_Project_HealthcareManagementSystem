    private void InsertDataToJTable() {
        
        consultationTestResults = infirmiries.getLabTestDirectory();
        ArrayList<Consultation> consultationsReports = consultationTestResults.getTest_results_List();
        DefaultTableModel model = (DefaultTableModel) jTableTestTable.getModel();
        model.setRowCount(0);
        
        for(Consultation consultation: consultationsReports)
        {
            model.addRow(new Object[]{consultation, consultation.getTest_price()});

        }
        jTableTestTable.setModel(model);
    }
