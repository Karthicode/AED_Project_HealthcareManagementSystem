    private void jButtonTestCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTestCreateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTableTestTable.getModel();
        ConsultationTestResults consultationTestResults = infirmiries.getLabTestDirectory();
        Consultation consultation = new Consultation();
        String test_name = jTextFieldTestsName.getText();
        String test_price = jTextFieldTestingPrice.getText();
        consultation.setTest_name(test_name);
        consultation.setTest_price(test_price);
        consultationTestResults.addTest(consultation);
        infirmiries.setLabTestDirectory(consultationTestResults);
        JOptionPane.showMessageDialog(this, "New Test Information has been added.");
        model.addRow(new Object[]{consultation,consultation.getTest_price()});
        remove_empty_input_Values();
    }//GEN-LAST:event_jButtonTestCreateActionPerformed
   
    

