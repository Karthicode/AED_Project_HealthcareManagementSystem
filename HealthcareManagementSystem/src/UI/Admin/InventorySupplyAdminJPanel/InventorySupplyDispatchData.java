    private void insertRecordsInJTable() {
        
        OrderDirectory orderDirectory = inventorySupply.getOrderDirectory();
        ArrayList<DispatchRequest> orderList = orderDirectory.getOrderList();
        DefaultTableModel model = (DefaultTableModel) jTableNewOrders.getModel();
        model.setRowCount(0);

        
        for(DispatchRequest order: orderList)
        {
            if(order.getRequest_processing_status().equals("new")){
                model.addRow(new Object[]{order, order.getFirstAidKitsTotalQuantity(), order.getRequested_supply_Amount(),order.getInventory_Id()});
            }
        }
        jTableNewOrders.setModel(model);
    }

    private void updateDispatcher() {
        DispatcherServiceDirectory deliveryAgencyDirectory = healthcareSystemOrganizationIntegration.getDispatcherServiceDirectory();
        ArrayList<DispatcherService> dispatcherServices = deliveryAgencyDirectory.getDeliveryAgencyList();
        for(DispatcherService deliveryAgency: dispatcherServices){
            ItemToggle.addItem(deliveryAgency.getDeliveryAgencyId());
        }
    }
