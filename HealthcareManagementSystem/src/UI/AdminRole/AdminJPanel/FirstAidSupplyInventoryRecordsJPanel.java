    private void InsertDataToJTable() {
        EnterpriseUserDirectory userAccountDirectory = healthcareSystemOrganizationIntegration.getUserAccDirectory();
        ArrayList<ConsumerRecords> usersList = userAccountDirectory.get_User_Details();
    
        DefaultTableModel model = (DefaultTableModel) jTableEmployee.getModel();
        model.setRowCount(0);
        for(ConsumerRecords userAccount: usersList)
        {   
            if(userAccount.getRole().toString() == "MedSupWarehouseAdmin"){
                model.addRow(new Object[]{userAccount,userAccount.getInventory_Supply().getInventory_Location_Name(),userAccount.getPassword()});
            }
            
        }
        
        jTableEmployee.setModel(model);
    }

    private void addDefaultvalues() {
        
        EnterpriseUserDirectory usersList = healthcareSystemOrganizationIntegration.getUserAccDirectory();
        consumerAccount = new ConsumerRecords("inventoryAccess", "password",new InventoryManagerRole());
        InventorySupply inventorySupply = new InventorySupply("inventoryAccess","Inventory");
        inventorySupplyModelDirectory = healthcareSystemOrganizationIntegration.getInventoryDirectory();
        inventorySupplyModelDirectory.addMedSupWarehouse(inventorySupply);
        consumerAccount.setInventory_Supply(inventorySupply);
        if(usersList.verifyUserUnique("inventoryAccess")){
            usersList.addConsumerData(consumerAccount);
        }
           
    }
    
