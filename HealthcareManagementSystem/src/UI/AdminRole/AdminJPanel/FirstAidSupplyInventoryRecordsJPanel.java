    private void CreateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTableEmployee.getModel();
        ArrayList<String> user_input = check_empty_field();
        inventorySupplyModelDirectory = healthcareSystemOrganizationIntegration.getInventoryDirectory();
        EnterpriseUserDirectory enterpriseUsers = healthcareSystemOrganizationIntegration.getUserAccDirectory();
        if(enterpriseUsers.verifyUserUnique(user_input.get(0))){
            
            
            consumerAccount = new ConsumerRecords(user_input.get(0), user_input.get(2), new InventoryManagerRole());
            
            InventorySupply inventorySupply = new InventorySupply(user_input.get(0), user_input.get(1));
            consumerAccount.setInventory_Supply(inventorySupply);
            inventorySupplyModelDirectory.addMedSupWarehouse(inventorySupply);
            
            enterpriseUsers.addConsumerData(consumerAccount);
            
            JOptionPane.showMessageDialog(this, "New Inventory supply data has been added.");
            model.addRow(new Object[]{consumerAccount,user_input.get(1),user_input.get(2)});
            remove_empty_input_Values();
        }
        else{
            JOptionPane.showMessageDialog(this, "Username already taken. Please use a new name.");
        }
    }//GEN-LAST:event_CreateButtonActionPerformed

    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        int selected_row_ix = jTableEmployee.getSelectedRow();

        if(selected_row_ix < 0){
            JOptionPane.showMessageDialog(this, "Please select rows before updating");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) jTableEmployee.getModel();
        ConsumerRecords consumer_acc_selected = (ConsumerRecords)model.getValueAt(selected_row_ix, 0);        
        UserAccountDirectory = healthcareSystemOrganizationIntegration.getUserAccDirectory();
        inventorySupplyModelDirectory = healthcareSystemOrganizationIntegration.getInventoryDirectory();
        ArrayList<ConsumerRecords> consumerRecordses = UserAccountDirectory.get_User_Details();
        for(ConsumerRecords userAcc: consumerRecordses)
        {
            if(userAcc.getEmail_Id().equals(consumer_acc_selected.getEmail_Id()))
            {
                InventorySupply inventorySupply = userAcc.getInventory_Supply();
                ArrayList<String> user_input = check_empty_field();
                model.setValueAt(user_input.get(1), selected_row_ix, 1);
                model.setValueAt(user_input.get(2), selected_row_ix, 2);
                inventorySupplyModelDirectory.updateMedSupWarehouse(user_input, inventorySupply);
                UserAccountDirectory.updateEnterpriseUser(set_user_input_values(userAcc, user_input));
                break;
            }
        }
        JOptionPane.showMessageDialog(this, "Updated Data");

        
    }//GEN-LAST:event_UpdateButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        int selected_row_ix = jTableEmployee.getSelectedRow();

        if(selected_row_ix < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTableEmployee.getModel();
        ConsumerRecords select_user_account_details = (ConsumerRecords)model.getValueAt(selected_row_ix, 0);
        UserAccountDirectory = healthcareSystemOrganizationIntegration.getUserAccDirectory();
        InventorySupply inventorySupply = select_user_account_details.getInventory_Supply();
        inventorySupplyModelDirectory.deleteMedSupWarehouse(inventorySupply);
        UserAccountDirectory.removeConsumerRecord(select_user_account_details);
        model.removeRow(selected_row_ix);
        InsertDataToJTable();
        remove_empty_input_Values();
    }//GEN-LAST:event_DeleteButtonActionPerformed

